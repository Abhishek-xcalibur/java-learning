public class Variables{
    public static void main(String[] args){
        /* Varibles are used to store data in Java.
        There are different types of variables in Java.
        String variable is used to store text data in Java.
        int variable is used to store integer values (whole numbers)
        float variable is used to store decimal values 
        char variable is used to store single character values
        boolean variable is used to store true or false values */

        String name= "Alice";
        System.out.println("My name is "+name);
        int age=25;
        System.out.println("My age is "+age);
        float height =5.6f;
        System.out.println("My height is "+height);
        boolean isStudent=true;
        System.out.println("I am a student: "+isStudent);
        char grade='A';
        System.out.println("My grade is " +grade);
        /* Data types in Java are used to define the type of data that a variable can hold.
        There are two types of data types in Java:
        1. Primitive data types: These are the basic data types in Java and include int, float, double, char, boolean, byte, short, and long.
        2. Non-primitive data: These are the data types that are derived from primitive data types and include String, Arrays, Classes, and Interfaces. 
        Non-primitive data types are also known as reference data types. */
    }
}

